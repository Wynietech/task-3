var opr = prompt("Enter the operation :* ,+  ,- , /  ")
var num1 = parseInt( prompt("Number1;  "))
var num2 = parseInt( prompt("Number2:  "))

if (opr == "+")
{
  alert(num1 + num2)
} else if (opr == "-")
{
   alert(num1 - num2)
} else if (opr == "*")
{
   alert(num1 * num2)
}else if (opr == "/")
{
   alert(num1 / num2)
}
